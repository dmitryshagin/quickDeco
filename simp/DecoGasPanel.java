    public void addAtom(String gas,Boolean active) {
        DecoGasAtom atom=new DecoGasAtom(gas,active);
        atom.jbRemove.addActionListener(new ActionListener(){

            @Override
            public void actionPerformed(ActionEvent ae) {
               removeAtom(ae);
            }
        });
        jpAtoms.add(atom);
        jpAtoms.revalidate();
        jpAtoms.repaint();
    }

