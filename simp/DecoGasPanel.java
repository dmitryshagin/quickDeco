    private void removeAtom(ActionEvent ae) {
        DecoGasAtom atom=(DecoGasAtom)((JButton)ae.getSource()).getParent();
        atom.clearDCD();
        jpAtoms.remove(((JButton)ae.getSource()).getParent());
        jpAtoms.revalidate();
        jpAtoms.repaint();
        aThis.updateDeco();
    }

    
